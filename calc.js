module.exports = {
  soma(n1,n2){
    console.log(`A soma de ${n1} mais ${n2} é: ${(n1+n2)}`)
  },
  subtracao(n1,n2){
    console.log(`A subtração de ${n1} menos ${n2} é: ${(n1-n2)}`)
  },
  multiplicacao(n1,n2){
    console.log(`A multiplicação entre ${n1} e ${n2} é: ${(n1*n2)}`)
  },
  divisao(n1,n2){
    console.log(`A divisão entre ${n1} e ${n2} é: ${(n1/n2)}`)
  }
}